# ------------------------------------前置任务脚本 ------------------------------------
import pandas as pd 
import numpy as np

# 读取固定资产台账
固定资产台账 = pd.read_excel('固定资产台账.xlsx')

# ------------------------------------开始任务 ------------------------------------
# ------------------------------------任务5 Python资产数据筛选 ------------------------------------

# 1. 数据筛选：仅保留2023-03期间、计算机及电子设备类别
固定资产台账 = 固定资产台账[固定资产台账['当前期间'] == '2023-03']
固定资产台账 = 固定资产台账[固定资产台账['固定资产类别'] == '计算机及电子设备']
固定资产台账 = 固定资产台账.loc[:, ['原始成本', '累计折旧', '累计减值', '账面净值']]
print("筛选后的固定资产数据：")
print(固定资产台账)

# 2. 确定待出售资产（折旧率 > 70%）
固定资产台账['折旧率'] = (固定资产台账['累计折旧'] / 固定资产台账['原始成本']).round(2)
待出售资产 = 固定资产台账[固定资产台账['折旧率'] > 0.7]
print("\n待出售资产（折旧率>70%）：")
print(待出售资产)
