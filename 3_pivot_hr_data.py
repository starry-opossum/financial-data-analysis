# ------------------------------------前置任务脚本 ------------------------------------
import pandas as pd 
import numpy as np

# 读取员工信息
员工信息 = pd.read_excel('员工信息.xlsx')

# 提取生日月份
生日月份 = []
for i in 员工信息['证件号']:    
    月份 = str(i)[10:12]
    生日月份.append(月份)
员工信息['生日月份'] = pd.DataFrame(生日月份)

# 定义部门性质映射
部门性质 = pd.DataFrame({
    '所属部门': ['长春销售部', '西安销售部', '南昌销售部', '济南销售部', '北京销售部',
              '行政部', '人力资源部', '法务部', '财务部',
              'ME系统部', 'ATS系统部', '产品运维部', 'PEM系统部'],
    '部门性质': ['销售', '销售', '销售', '销售', '销售',
              '管理', '管理', '管理', '管理',
              '生产', '生产', '生产', '研发']
})

# 合并部门性质信息
员工信息 = pd.merge(员工信息, 部门性质, on='所属部门', how='left')

# ------------------------------------开始任务 ------------------------------------
# ------------------------------------任务8 Python人力数据透视 ------------------------------------

# 1. 创建透视表
透视表 = pd.pivot_table(
    员工信息,
    index='生日月份',
    columns='部门性质',
    values='证件号',  # 这里使用证件号计数
    aggfunc='count',
    fill_value=0
)

# 2. 计算每个月份、每个部门的金额（每人500元福利费）
金额透视表 = 透视表 * 500

# 3. 全年各部门性质总金额和总金额
部门合计 = 金额透视表.sum()
总金额 = 部门合计.sum()
print('部门合计：\n', 部门合计)
print('总金额：', 总金额)

# 4. 月度总计
金额透视表['合计'] = 金额透视表.sum(axis=1)
print('\n金额透视表：\n', 金额透视表)
