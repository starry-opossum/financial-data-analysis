import pandas as pd
import numpy as np
from pathlib import Path

# 读取完整台账并重复筛选（也可以直接读取上一步筛选后的结果，但为了独立运行，这里重新筛选）
当前目录 = Path(__file__).parent
固定资产台账 = pd.read_excel(当前目录 / '固定资产台账.xlsx')

# 筛选（与 2_filter_assets.py 中一致）
固定资产台账 = 固定资产台账[固定资产台账['当前期间'] == '2023-03']
固定资产台账 = 固定资产台账[固定资产台账['固定资产类别'] == '计算机及电子设备']
固定资产台账 = 固定资产台账.loc[:, ['原始成本', '累计折旧', '累计减值', '账面净值']]
固定资产台账['折旧率'] = (固定资产台账['累计折旧'] / 固定资产台账['原始成本']).round(2)
待出售资产 = 固定资产台账[固定资产台账['折旧率'] > 0.7]

print("=" * 50)
print("设备更新成本测算报告")
print("=" * 50)

# 1. 折旧率超过70%的电子产品账面原值合计
待出售资产原值 = 待出售资产['原始成本'].sum()
print(f'待出售资产原值合计：{待出售资产原值:,.2f}元')

# 2. 累计折旧合计
待出售资产累计折旧 = 待出售资产['累计折旧'].sum()
print(f'待出售资产累计折旧合计：{待出售资产累计折旧:,.2f}元')

# 3. 累计减值合计
待出售资产累计减值 = 待出售资产['累计减值'].sum()
print(f'待出售资产累计减值合计：{待出售资产累计减值:,.2f}元')

# 4. 账面净值合计
待出售资产账面净值 = 待出售资产['账面净值'].sum()
print(f'待出售资产账面净值合计：{待出售资产账面净值:,.2f}元')

# 5. 预计可回收金额（按原价10%估算）
回收价合计 = (待出售资产['原始成本'] * 0.1).sum()
print(f'预计回收金额合计（原价10%）：{回收价合计:,.2f}元')

# 6. 重新购置预算（按原价1.1倍估算）
新购价格合计 = (待出售资产['原始成本'] * 1.1).sum()
print(f'重新购置预算合计（原价1.1倍）：{新购价格合计:,.2f}元')

print("\n" + "=" * 50)
print("测算完成")
print("=" * 50)